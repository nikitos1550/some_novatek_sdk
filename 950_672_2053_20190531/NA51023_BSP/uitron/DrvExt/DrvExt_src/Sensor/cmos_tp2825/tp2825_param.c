SENSOR_MODE_INFO *get_mode_info_tp2825(SENSOR_MODE mode)
{
	if (mode >= (MODE_MAX + 1)) {
		DBG_ERR("sensor mode %d > Max(%d), force to mode 0\r\n", mode, MODE_MAX);
		//return NULL;
		mode = 0;
	}
	return &mode_tp2825[mode];
}

UINT32 get_cmd_tab_tp2825(SENSOR_MODE mode, SENSOR_CMD **cmd_tab)
{
	switch (mode) {
	case SENSOR_MODE_1:
		*cmd_tab = tp2825_mode_1;
		return sizeof(tp2825_mode_1) / sizeof(SENSOR_CMD);

	default:
		DBG_ERR("sensor mode %d no cmd table\r\n", mode);
		*cmd_tab = NULL;
		return 0;
	}
}

